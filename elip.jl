        ind = Int(floor(x*100))
        # x_elip[ind] <= x <= x_elip[ind+1]
        h = x - x_elip[ind]
        k = dx - h
        # println(x_elip[ind],"  ",x_elip[ind+1])
        # println(elip[ind],"  ",elip[ind+1])
        dx_i*(y_elip[ind]*k + y_elip[ind+1]*h)
    else
        0.0
    end
end

